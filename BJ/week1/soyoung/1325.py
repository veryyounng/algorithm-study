import sys
from collections import deque

# ğŸ”¹ ì…ë ¥ë°›ê¸°
input = sys.stdin.readline
n, m = map(int, input().split())  # n: ì»´í“¨í„° ìˆ˜, m: ì‹ ë¢° ê´€ê³„ ìˆ˜

# ê·¸ë˜í”„ ì´ˆê¸°í™”: ì»´í“¨í„° ë²ˆí˜¸ëŠ” 1ë²ˆë¶€í„° ì‹œì‘í•˜ë¯€ë¡œ n+1 í¬ê¸°ë¡œ ìƒì„±
graph = [[] for _ in range(n + 1)]

# ğŸ”¹ ì‹ ë¢° ê´€ê³„ ì…ë ¥ ë°›ê¸°
# A B: Aê°€ Bë¥¼ ì‹ ë¢°í•œë‹¤ â†’ Bë¥¼ í•´í‚¹í•˜ë©´ Aë„ í•´í‚¹ ê°€ëŠ¥ â†’ ê°„ì„  ë°©í–¥ì€ B â†’ A
for _ in range(m):
    a, b = map(int, input().split())
    graph[b].append(a)  # ì—­ë°©í–¥ ê°„ì„  ì €ì¥

# ğŸ”¹ BFS í•¨ìˆ˜ ì •ì˜
def bfs(start):
    visited = [False] * (n + 1)  # ë°©ë¬¸ ì—¬ë¶€ ì²´í¬
    queue = deque([start])      # ì‹œì‘ ë…¸ë“œë¥¼ íì— ë„£ìŒ
    visited[start] = True
    count = 1  # í•´í‚¹ ê°€ëŠ¥í•œ ì»´í“¨í„° ìˆ˜ (ìê¸° ìì‹  í¬í•¨)

    while queue:
        now = queue.popleft()
        # í˜„ì¬ ë…¸ë“œ(now)ë¥¼ í•´í‚¹í–ˆì„ ë•Œ ì—°ê²°ëœ ë‹¤ë¥¸ ë…¸ë“œë¥¼ í™•ì¸
        for next_node in graph[now]:
            if not visited[next_node]:  # ì•„ì§ ë°©ë¬¸í•˜ì§€ ì•Šì•˜ë‹¤ë©´
                visited[next_node] = True  # ë°©ë¬¸ ì²˜ë¦¬
                queue.append(next_node)   # íì— ì¶”ê°€
                count += 1                # í•´í‚¹ ê°€ëŠ¥í•œ ì»´í“¨í„° ìˆ˜ ì¦ê°€
    return count

# ğŸ”¹ ëª¨ë“  ë…¸ë“œë¥¼ í•´í‚¹ ì‹œì‘ì ìœ¼ë¡œ ê°€ì •í•˜ì—¬ BFS ëŒë¦¬ê¸°
max_count = 0        # í˜„ì¬ê¹Œì§€ í•´í‚¹ ê°€ëŠ¥í•œ ìµœëŒ€ ì»´í“¨í„° ìˆ˜
result = []          # ìµœëŒ€ê°’ì„ ë§Œë“  ì»´í“¨í„° ë²ˆí˜¸ë“¤ì„ ì €ì¥

for i in range(1, n + 1):
    count = bfs(i)   # ië²ˆ ì»´í“¨í„°ë¥¼ í•´í‚¹ ì‹œì‘ì ìœ¼ë¡œ í–ˆì„ ë•Œ ê°ì—¼ ìˆ˜
    if count > max_count:
        max_count = count      # ìƒˆë¡œìš´ ìµœëŒ€ê°’ì´ë©´ ê°±ì‹ 
        result = [i]           # ê²°ê³¼ ë¦¬ìŠ¤íŠ¸ ì´ˆê¸°í™”
    elif count == max_count:
        result.append(i)       # ìµœëŒ€ê°’ê³¼ ê°™ë‹¤ë©´ ê²°ê³¼ ë¦¬ìŠ¤íŠ¸ì— ì¶”ê°€

# ğŸ”¹ ê²°ê³¼ ì¶œë ¥
# sorted(result): ì˜¤ë¦„ì°¨ìˆœ ì •ë ¬ëœ ë¦¬ìŠ¤íŠ¸ ë°˜í™˜
# *result: ë¦¬ìŠ¤íŠ¸ë¥¼ ì–¸íŒ©í•˜ì—¬ ê³µë°±ìœ¼ë¡œ êµ¬ë¶„í•´ print
print(*sorted(result))  # ì˜ˆ: [1, 2, 3] â†’ ì¶œë ¥: 1 2 3